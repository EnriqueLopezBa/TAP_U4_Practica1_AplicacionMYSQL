    private void cargarDatos(){
        m.setRowCount(0);
        try(ResultSet rs = con.Consulta("SELECT * FROM PRODUCTOS")) {
            
            while(rs.next()){
                m.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)});
            }
        } catch (Exception e) {
            showMessageDialog(null, e.getMessage());
        }
        
    }

