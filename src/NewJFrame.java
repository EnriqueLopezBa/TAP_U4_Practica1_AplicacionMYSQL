    private void btnInsertaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertaActionPerformed
        try(PreparedStatement ps = cn.prepareStatement("INSERT INTO PRODUCTOS(descripcion,precio,existencia) VALUES(?,?,?)")) {
            ps.setString(1, txtDescri.getText());
            ps.setFloat(2, Float.parseFloat(txtPrecio.getText()));
            ps.setInt(3, Integer.parseInt(txtExistencia.getText()));
            ps.executeUpdate();
            showMessageDialog(null, "Se inserto correctamente");
            cargarDatos();
        } catch (Exception e) {
            showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnInsertaActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = jTable1.getSelectedRow();
        if(fila != -1){
             try(PreparedStatement ps = cn.prepareStatement("UPDATE PRODUCTOS SET DESCRIPCION = '"+txtDescri.getText()
                +"',PRECIO = "+Float.parseFloat(txtPrecio.getText())+",EXISTENCIA = "+Integer.parseInt(txtExistencia.getText())+" WHERE IDPRODUCTOS ="+Integer.parseInt(m.getValueAt(fila, 0).toString()))) {
            ps.executeUpdate();
                 showMessageDialog(null, "Se actualizo correctamente");
                 cargarDatos();
                 btnInserta.setEnabled(true);
                 btnActualizar.setEnabled(false);
        } catch (Exception e) {
                 showMessageDialog(null, e.getMessage());
        }
        }else{
            showMessageDialog(null, "Selecciona una fila");
        }
       
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = jTable1.getSelectedRow();
        if(fila != -1){
            try(PreparedStatement ps = cn.prepareStatement("DELETE FROM PRODUCTOS WHERE IDPRODUCTOS = "+Integer.parseInt(m.getValueAt(fila,0).toString()))) {
                ps.executeUpdate();
                showMessageDialog(null, "Se elimino correcamente");
                cargarDatos();
            } catch (Exception e) {
                showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        int fila = jTable1.getSelectedRow();
        if(fila != -1){
            txtDescri.setText(m.getValueAt(fila, 1).toString());
            txtPrecio.setText(m.getValueAt(fila, 2).toString());
            txtExistencia.setText(m.getValueAt(fila, 3).toString());
            btnActualizar.setEnabled(true);
            btnInserta.setEnabled(false);
        }
        
    }//GEN-LAST:event_btnCargarActionPerformed

